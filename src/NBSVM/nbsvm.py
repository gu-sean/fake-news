def predict_news(path):
    crawl_filename = os.path.join(path, "realtime_news.csv")
    if not os.path.exists(crawl_filename): return print("🚨 크롤링 데이터 없음!")

    crawl_df = pd.read_csv(crawl_filename)
    try:
        scaler = joblib.load(os.path.join(path, 'best_scaler_svm.pkl'))
        model = joblib.load(os.path.join(path, 'best_model_svm.pkl'))
        best_threshold = joblib.load(os.path.join(path, 'best_threshold_svm.pkl'))
        r_vector = joblib.load(os.path.join(path, 'best_nb_r_vector.pkl'))
        tfidf_word = joblib.load(os.path.join(path, 'best_vec_word.pkl'))
        tfidf_char = joblib.load(os.path.join(path, 'best_vec_char.pkl'))
        selector = joblib.load(os.path.join(path, 'best_selector.pkl')) # 🔥 Feature Selector 로드
    except Exception as e: return print(f"🚨 파일 로드 실패! 훈련을 먼저 진행해주세요.\n에러: {e}")

    text_col = 'clean_message' if 'clean_message' in crawl_df.columns else 'text'
    X_real_text = (crawl_df['title'].fillna("").astype(str) + " ") * 5 + crawl_df[text_col].fillna("").astype(str)

    print("🔄 1. 실전 기사 거대 피처 변환 중...")
    X_real_word = tfidf_word.transform(X_real_text)
    X_real_char = tfidf_char.transform(X_real_text)
    X_real_massive = hstack([X_real_word, X_real_char]).tocsr()

    print("🧲 2. 카이제곱(Chi-Square) 필터링 및 NBSVM 가중치 적용 중...")
    # 훈련 때 골라둔 40,000개의 진성 피처만 뽑아냄
    X_real_selected = selector.transform(X_real_massive)
    X_real_tfidf = X_real_selected.multiply(r_vector).tocsr()

    print("🧩 3. 메타 패턴 추출 및 스케일링 중...")
    real_meta_scaled = scaler.transform(extract_dvl_features(crawl_df))
    X_real_combined = hstack([X_real_tfidf, csr_matrix(real_meta_scaled)]).tocsr()

    print("🎯 판별 진행 중...")
    decision_scores = model.decision_function(X_real_combined)

    crawl_df['predicted_label'] = (decision_scores > best_threshold).astype(int)
    probs = 1 / (1 + np.exp(-decision_scores))
    crawl_df['fake_probability(%)'] = np.round(probs * 100, 2)
    crawl_df['결과_텍스트'] = crawl_df['predicted_label'].map({0: '✅ 진짜뉴스', 1: '🚨 가짜뉴스'})

    out_file = os.path.join(path, "realtime_news_svm_result.csv")
    crawl_df.to_csv(out_file, index=False, encoding='utf-8-sig')
    print(f"🎉 판별 완료! 결과: {os.path.basename(out_file)}")

